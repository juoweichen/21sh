#include "../../includes/libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	j;

	if (s2 == NULL)
		return (s1);
	i = ft_strlen(s1);
	j = 0;
	while (s2[j])
		s1[i++] = s2[j++];
	s1[i] = '\0';
	return (s1);
}
