#include "../../includes/libft.h"

int		ft_numlen(int n)
{
	int		len;
	long	ln;

	ln = (long)n;
	len = 0;
	if (ln < 0)
	{
		ln = ln * -1;
		len++;
	}
	while (ln > 0)
	{
		ln = ln / 10;
		len++;
	}
	return (len);
}
